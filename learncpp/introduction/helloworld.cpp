// The first line indicates preprocessor directive. The preprocessor directive indicates that we would like to use
// the contents of the iostream library, which is a part of the C++ standard library that helps us read and write text
// into the command line.
#include <iostream>

int main()
{
    // std::cout stands for "character output" and << operator allows us to send letters or numbers to the console.
    std::cout << "Hello, World!" << std::endl;
    return 0;
}
